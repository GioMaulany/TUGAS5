package com.example.tugas5;

public class TeamModel
{
   private String team_name,team_desc;
   private int team_logo;

    public String getTeam_name() {
        return team_name;
    }

    public void setTeam_name(String team_name) {
        this.team_name = team_name;
    }

    public String getTeam_desc() {
        return team_desc;
    }

    public void setTeam_desc(String team_desc) {
        this.team_desc = team_desc;
    }

    public int getTeam_logo() {
        return team_logo;
    }

    public void setTeam_logo(int team_logo) {
        this.team_logo = team_logo;
    }
}
